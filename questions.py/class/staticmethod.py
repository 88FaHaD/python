class Calculator:
    @staticmethod
    def add(a,b):
     return a+b
    @staticmethod
    def sub(a,b):
     return a-b
    @staticmethod
    def div(a,b):
     return a/b
    @staticmethod
    def mul(a,b):
     return a*b
    
i=3
j=4

print(Calculator.add(i,j))

print(Calculator.sub(i,j))

print(Calculator.mul(i,j))

print(Calculator.div(i,j))

    
